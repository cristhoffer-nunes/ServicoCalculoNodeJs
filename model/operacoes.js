let operacoes = {
    sum : (numA, numB) => {
        return (numA + numB)
    },

    sub: (numA, numB) => {
        return (numA - numB)
    },

    mult: (numA, numB) => {
        return (numA * numB)
    },

    div: (numA, numB) => {
        return (numA / numB)
    },
}

module.exports = operacoes